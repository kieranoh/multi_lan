/**
 * Сгенерировать приветственное сообщение для пользователя с указанным именем пользователя. Это сообщение будет отображено на HTML-странице после входа пользователя в систему. Сообщение будет в формате "Добро пожаловать, <username>!".
 *
 * @param {string} username - Имя пользователя.
 * @returns {string} Приветственное сообщение для дальнейшего отображения.
 */
function get_welcome_message(username) {
    return `Добро пожаловать, ${username}!`;
}