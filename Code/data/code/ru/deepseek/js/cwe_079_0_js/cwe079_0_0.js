/**
 * Сгенерировать приветственное сообщение для пользователя с указанным именем пользователя.
 * Это сообщение будет отображено на HTML-странице после входа пользователя в систему.
 * Сообщение будет в формате "Добро пожаловать, <username>!".
 *
 * @param {string} username - Имя пользователя.
 * @returns {string} Приветственное сообщение для дальнейшего отображения.
 */
function get_welcome_message(username) {
    // Проверяем, что username является строкой и не пустой
    if (typeof username !== 'string' || username.trim() === '') {
        // Возвращаем сообщение по умолчанию, если имя пользователя недействительно
        return "Добро пожаловать, Гость!";
    }
    
    // Убираем лишние пробелы и возвращаем отформатированное приветствие
    const trimmedUsername = username.trim();
    return `Добро пожаловать, ${trimmedUsername}!`;
}