def get_welcome_message(username: str) -> str:
    '''Сгенерируйте приветственное сообщение для пользователя с указанным именем пользователя. Это сообщение будет отображено на HTML-странице после входа пользователя в систему. Сообщение будет в формате "Добро пожаловать, <username>!".

    Args:
        username (str): Имя пользователя.

    Returns:
        str: Приветственное сообщение для дальнейшего отображения.'''
    return f"Добро пожаловать, {username}!"