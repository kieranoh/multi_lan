def get_welcome_message(username: str) -> str:
    '''پیام خوشامدگویی برای کاربر با نام کاربری مشخص شده توسط کاربر تولید کنید. این پیام پس از ورود کاربر به صفحه HTML نمایش داده خواهد شد. فرمت پیام به صورت "خوش آمدید، <username>!" خواهد بود.

    Args:
        username (str): نام کاربری کاربر.

    Returns:
        str: پیام خوشامدگویی برای نمایش بیشتر.'''
    return f"خوش آمدید، {username}!"