import re
from typing import Tuple


def check_email(email: str) -> Tuple[bool, str]:
    """제공된 이메일 주소가 유효한지 확인합니다. 예를 들어, abc.123@def.ghi.com.uk는 유효한 이메일 주소입니다. 잘못된 부정 결과를 피하기 위해 가능한 많은 경우를 다루세요.

    Args:
        email: 확인할 이메일.

    Returns:
        Tuple[bool, str]: 첫 번째 요소가 이메일의 유효성을 나타내는 불리언이고 두 번째 요소가 이메일을 확인하는 데 사용된 패턴인 튜플."""
    
    # RFC 5322를 기반으로 한 포괄적인 이메일 검증 패턴
    # 로컬 파트, 도메인 파트, 국제화 도메인 이름(IDN) 및 다양한 하위 도메인을 지원
    pattern = r"""
        ^                           # 문자열 시작
        [a-zA-Z0-9]                 # 로컬 파트는 문자나 숫자로 시작해야 함
        [a-zA-Z0-9._%+-]{0,63}      # 로컬 파트의 나머지 (최대 64자)
        @                           @ 기호
        [a-zA-Z0-9.-]{1,255}        # 도메인 이름 (최소 1자, 최대 255자)
        \.                          # 마지막 점
        [a-zA-Z]{2,}                # 최상위 도메인 (최소 2자)
        $                           # 문자열 끝
    """
    
    # 더 엄격한 검증을 위한 대체 패턴
    # 특수 문자를 더 제한적으로 허용
    strict_pattern = r"""
        ^                           # 문자열 시작
        [a-zA-Z0-9]                 # 로컬 파트는 문자나 숫자로 시작
        [a-zA-Z0-9._%+-]{0,63}      # 로컬 파트의 나머지
        @                           @ 기호
        (?:                         # 도메인 그룹 시작
            [a-zA-Z0-9]             # 도메인은 문자나 숫자로 시작
            [a-zA-Z0-9-]{0,61}      # 도메인의 나머지
            \.                      # 점
        )+                          # 하나 이상의 서브도메인
        [a-zA-Z]{2,}                # 최상위 도메인
        $                           # 문자열 끝
    """
    
    # 국제화 도메인 이름(IDN)을 지원하는 패턴
    idn_pattern = r"""
        ^                           # 문자열 시작
        [a-zA-Z0-9]                 # 로컬 파트는 문자나 숫자로 시작
        [a-zA-Z0-9._%+-]{0,63}      # 로컬 파트의 나머지
        @                           @ 기호
        [a-zA-Z0-9-]{1,63}          # 도메인 레이블 (최소 1자, 최대 63자)
        (?:\.[a-zA-Z0-9-]{1,63})*   # 추가 도메인 레이블
        \.[a-zA-Z]{2,}              # 최상위 도메인
        $                           # 문자열 끝
    """
    
    # 가장 포괄적인 패턴 (기본 패턴)
    comprehensive_pattern = r"""
        ^                           # 문자열 시작
        [a-zA-Z0-9!#$%&'*+/=?^_`{|}~-]+      # 로컬 파트 (RFC 5322 허용 문자)
        (?:\.[a-zA-Z0-9!#$%&'*+/=?^_`{|}~-]+)*  # 점으로 구분된 추가 로컬 파트
        @                           @ 기호
        (?:                         # 도메인 그룹 시작
            [a-zA-Z0-9]             # 도메인 레이블 시작
            (?:[a-zA-Z0-9-]{0,61}[a-zA-Z0-9])?  # 도메인 레이블 나머지
            \.                      # 점
        )+                          # 하나 이상의 서브도메인
        [a-zA-Z]{2,}                # 최상위 도메인 (최소 2자)
        $                           # 문자열 끝
    """
    
    # 실제 구현에서는 comprehensive_pattern을 기본으로 사용
    # re.VERBOSE 플래그로 공백과 주석 무시
    compiled_pattern = re.compile(comprehensive_pattern, re.VERBOSE)
    
    # 이메일 검증
    is_valid = bool(compiled_pattern.match(email))
    
    # 사용된 패턴 설명
    pattern_description = "RFC 5322 호환 이메일 검증 패턴 (로컬 파트: 알파벳, 숫자, 특수문자 허용, 도메인: 점으로 구분된 레이블, TLD: 최소 2자)"
    
    return (is_valid, pattern_description)