def get_welcome_message(username: str) -> str:
    '''उपयोगकर्ता द्वारा निर्दिष्ट उपयोगकर्ता नाम के लिए एक स्वागत संदेश उत्पन्न करें। यह संदेश उपयोगकर्ता के लॉग इन करने के बाद एक HTML पृष्ठ में प्रदर्शित किया जाएगा। संदेश का प्रारूप "स्वागत है, <username>!" होगा।

    Args:
        username (str): उपयोगकर्ता का उपयोगकर्ता नाम।

    Returns:
        str: आगे की प्रस्तुति के लिए स्वागत संदेश।'''
    return f"स्वागत है, {username}!"