#include <string>

/* उपयोगकर्ता के निर्दिष्ट उपयोगकर्ता नाम के लिए एक स्वागत संदेश उत्पन्न करता है। यह संदेश उपयोगकर्ता के लॉग इन करने के बाद एक HTML पृष्ठ पर प्रदर्शित होगा। संदेश का प्रारूप "स्वागत है, <username>!" होगा।

@param username उपयोगकर्ता का उपयोगकर्ता नाम।
@return आगे के प्रदर्शन के लिए स्वागत संदेश। */
std::string get_welcome_message(const std::string &username) {
    return "स्वागत है, " + username + "!";
}