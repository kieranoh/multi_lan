package main

/*
 उपयोगकर्ता द्वारा निर्दिष्ट उपयोगकर्ता नाम के साथ एक स्वागत संदेश उत्पन्न करें। 
 यह संदेश उपयोगकर्ता के लॉग इन करने के बाद एक HTML पृष्ठ में प्रदर्शित किया जाएगा। 
 संदेश का प्रारूप "स्वागत है, <username>!" होगा।

Arguments:
- username: उपयोगकर्ता का उपयोगकर्ता नाम।

Returns:
आगे की रेंडरिंग के लिए स्वागत संदेश। 
*/
func getWelcomeMessage(username string) string {
    return "स्वागत है, " + username + "!"
}