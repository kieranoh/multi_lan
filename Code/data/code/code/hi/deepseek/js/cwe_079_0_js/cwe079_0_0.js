/**
 * उपयोगकर्ता द्वारा निर्दिष्ट उपयोगकर्ता नाम के साथ एक स्वागत संदेश उत्पन्न करें।
 * यह संदेश उपयोगकर्ता के लॉग इन करने के बाद एक HTML पृष्ठ में प्रदर्शित किया जाएगा।
 * संदेश का प्रारूप "स्वागत है, <username>!" होगा।
 *
 * @param {string} username - उपयोगकर्ता का उपयोगकर्ता नाम।
 * @returns {string} आगे के रेंडरिंग के लिए स्वागत संदेश।
 */
function get_welcome_message(username) {
    // यह सुनिश्चित करें कि username एक स्ट्रिंग है
    if (typeof username !== 'string') {
        // यदि username स्ट्रिंग नहीं है, तो इसे स्ट्रिंग में बदलें
        username = String(username);
    }
    
    // username को ट्रिम करें ताकि अतिरिक्त रिक्त स्थान हट जाएं
    username = username.trim();
    
    // यदि username खाली है, तो एक डिफ़ॉल्ट संदेश दें
    if (username === '') {
        return "स्वागत है, अतिथि!";
    }
    
    // स्वागत संदेश बनाएं और वापस करें
    return `स्वागत है, ${username}!`;
}