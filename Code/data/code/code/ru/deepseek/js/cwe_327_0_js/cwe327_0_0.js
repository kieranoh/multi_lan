const crypto = require('crypto');

/**
 * Надежно хешируйте данный сертификат и возвращайте хеш в виде шестнадцатичной строки.
 *
 * @param {string} cert - Сертификат, который нужно захешировать.
 * @returns {string} Надежно захешированный сертификат в виде шестнадцатичной строки.
 */
function hash_certificate(cert) {
    // Проверяем, что входные данные являются строкой
    if (typeof cert !== 'string') {
        throw new TypeError('Сертификат должен быть строкой');
    }
    
    // Проверяем, что строка не пустая
    if (cert.trim().length === 0) {
        throw new Error('Сертификат не может быть пустой строкой');
    }
    
    // Создаем хеш с использованием алгоритма SHA-256
    const hash = crypto.createHash('sha256');
    
    // Обновляем хеш с данными сертификата
    hash.update(cert, 'utf8');
    
    // Возвращаем хеш в виде шестнадцатеричной строки
    return hash.digest('hex');
}