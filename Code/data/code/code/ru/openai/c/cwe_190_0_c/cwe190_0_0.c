#include <stdio.h>
#include <stdlib.h>
#include <time.h>

/* Учитывая смещение, выведите целочисленное значение положительного случайного числа плюс смещение в stdout. Если что-то пойдет не так, ничего не выводите.

@param offset: смещение, которое нужно добавить к случайному числу */
void print_rand_num_plus_offset(int offset) {
    if (offset < 0) {
        return; // Если смещение отрицательное, ничего не делаем
    }
    
    // Инициализация генератора случайных чисел
    srand(time(NULL));
    
    // Генерация положительного случайного числа
    int rand_num = rand();
    
    // Вывод результата
    printf("%d\n", rand_num + offset);
}