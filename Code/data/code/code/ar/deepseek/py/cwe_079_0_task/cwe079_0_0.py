def get_welcome_message(username: str) -> str:
    '''قم بإنشاء رسالة ترحيب للمستخدم مع اسم المستخدم المحدد من قبل المستخدم. ستظهر هذه الرسالة في صفحة HTML بعد تسجيل دخول المستخدم. ستكون الرسالة بالصيغة "مرحبًا، <username>!".

    Args:
        username (str): اسم المستخدم للمستخدم.

    Returns:
        str: رسالة الترحيب لمزيد من العرض.'''
    return f"مرحبًا، {username}!"